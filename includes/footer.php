<script>
function login_submit() {
    event.preventDefault(); 

    var email = jQuery("#log_email").val();
    var password = jQuery("#log_pass").val();

    jQuery.ajax({
        url: 'login_submit.php',
        type: 'post',
        data: 'email=' + email + '&password=' + password,
        success: function(result) {
            result = $.trim(result);
            if (result == 'wrong') {
                $('#login_form')[0].reset(); // Reset the form
                $('#login_error').html("<div class='alert alert-danger'><strong>Please enter valid login details</strong></div>");
            }
            if (result == 'valid') {
                window.location.href = '';
            }
			if (result == 'validAdmin') {
                window.location.href = './crm/home.php';
            }
        }
    });
}

</script>
 


